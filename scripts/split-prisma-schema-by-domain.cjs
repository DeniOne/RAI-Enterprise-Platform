#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const root = path.resolve(__dirname, '..');
const schemaPath = path.join(root, 'packages/prisma-client/schema.prisma');
const manifestPath = path.join(root, 'docs/01_ARCHITECTURE/DATABASE/MODEL_SCOPE_MANIFEST.md');
const fragmentsDir = path.join(root, 'packages/prisma-client/schema-fragments');

const fragmentOrder = [
  '00_base.prisma',
  '01_platform_core.prisma',
  '02_org_legal.prisma',
  '03_agri_planning.prisma',
  '04_agri_execution.prisma',
  '05_finance.prisma',
  '06_crm_commerce.prisma',
  '07_ai_runtime.prisma',
  '08_integration_reliability.prisma',
  '09_quarantine_sandbox.prisma',
  '10_legacy_bridge.prisma',
];

const ownerToFragment = new Map([
  ['platform_core', '01_platform_core.prisma'],
  ['org_legal', '02_org_legal.prisma'],
  ['agri_planning', '03_agri_planning.prisma'],
  ['agri_execution', '04_agri_execution.prisma'],
  ['finance', '05_finance.prisma'],
  ['crm_commerce', '06_crm_commerce.prisma'],
  ['ai_runtime', '07_ai_runtime.prisma'],
  ['integration_reliability', '08_integration_reliability.prisma'],
  ['quarantine_sandbox', '09_quarantine_sandbox.prisma'],
  ['legacy_bridge', '10_legacy_bridge.prisma'],
]);

function unwrap(v) {
  const s = String(v || '').trim();
  const m = s.match(/`([^`]+)`/);
  return (m ? m[1] : s).trim();
}

function parseOwnerMap(manifest) {
  const lines = manifest.split(/\r?\n/).filter((l) => l.trim().startsWith('|'));
  if (lines.length < 3) return new Map();

  const headers = lines[0].split('|').slice(1, -1).map((c) => c.trim());
  const rows = new Map();

  for (const line of lines.slice(2)) {
    if (/^\|[\s:-]+\|$/.test(line.trim())) continue;
    const cells = line.split('|').slice(1, -1).map((c) => c.trim());
    if (cells.length !== headers.length) continue;

    const row = {};
    headers.forEach((h, i) => { row[h] = cells[i]; });

    const model = unwrap(row['Model']);
    const ownerRaw = unwrap(row['Owner domain']);
    if (!model || !ownerRaw) continue;

    const ownerTop = ownerRaw.split('/')[0];
    rows.set(model, ownerTop);
  }

  return rows;
}

function parseBlocks(schemaText, kind) {
  const re = new RegExp(`^${kind}\\s+(\\w+)\\s*\\{[\\s\\S]*?^\\}`, 'gm');
  const blocks = [];
  let m;
  while ((m = re.exec(schemaText)) !== null) {
    blocks.push({ name: m[1], block: m[0].trim() + '\n' });
  }
  return blocks;
}

function extractHeader(schemaText) {
  const firstMatch = schemaText.match(/^(enum|model)\s+\w+\s*\{/m);
  if (!firstMatch || firstMatch.index == null) return schemaText.trim() + '\n';
  return schemaText.slice(0, firstMatch.index).trimEnd() + '\n\n';
}

function main() {
  const schema = fs.readFileSync(schemaPath, 'utf8');
  const manifest = fs.readFileSync(manifestPath, 'utf8');
  const ownerMap = parseOwnerMap(manifest);

  const header = extractHeader(schema);
  const models = parseBlocks(schema, 'model');
  const enums = parseBlocks(schema, 'enum');

  const fragmentBuckets = new Map(fragmentOrder.map((f) => [f, { enums: [], models: [] }]));

  for (const model of models) {
    const owner = ownerMap.get(model.name) || 'platform_core';
    const file = ownerToFragment.get(owner) || '00_base.prisma';
    fragmentBuckets.get(file).models.push(model);
  }

  // Enum ownership heuristic: if referenced mostly by models from one fragment -> that fragment, else base.
  for (const en of enums) {
    const counts = new Map();

    for (const file of fragmentOrder) {
      counts.set(file, 0);
    }

    for (const [file, bucket] of fragmentBuckets.entries()) {
      for (const model of bucket.models) {
        const rx = new RegExp(`\\b${en.name}\\b`);
        if (rx.test(model.block)) {
          counts.set(file, (counts.get(file) || 0) + 1);
        }
      }
    }

    let bestFile = '00_base.prisma';
    let bestCount = 0;
    let tie = false;

    for (const [file, count] of counts.entries()) {
      if (count > bestCount) {
        bestCount = count;
        bestFile = file;
        tie = false;
      } else if (count > 0 && count === bestCount) {
        tie = true;
      }
    }

    if (bestCount === 0 || tie) {
      bestFile = '00_base.prisma';
    }

    fragmentBuckets.get(bestFile).enums.push(en);
  }

  fs.mkdirSync(fragmentsDir, { recursive: true });

  for (const file of fragmentOrder) {
    const bucket = fragmentBuckets.get(file);
    const title = `// AUTO-GENERATED by scripts/split-prisma-schema-by-domain.cjs\n` +
      `// Fragment: ${file}\n\n`;

    const parts = [];
    if (file === '00_base.prisma') {
      parts.push(header);
      parts.push('// Shared primitives only: generator/datasource + shared enums/models.\n\n');
    }

    if (bucket.enums.length > 0) {
      parts.push('// Enums\n\n');
      parts.push(bucket.enums.map((e) => e.block).join('\n'));
      parts.push('\n');
    }

    if (bucket.models.length > 0) {
      parts.push('// Models\n\n');
      parts.push(bucket.models.map((m) => m.block).join('\n'));
      parts.push('\n');
    }

    const out = title + parts.join('');
    fs.writeFileSync(path.join(fragmentsDir, file), out, 'utf8');
  }

  console.log(`Fragments generated at ${fragmentsDir}`);
  for (const file of fragmentOrder) {
    const bucket = fragmentBuckets.get(file);
    console.log(`${file}: enums=${bucket.enums.length} models=${bucket.models.length}`);
  }
}

main();
